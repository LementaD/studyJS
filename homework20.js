//chess_________________________________________________________________________________________________________________________________________________
{
    function generateChessboard(rows, columns) {
        let result = '';
      
        for (let i = 0; i < rows; i++) {
          for (let j = 0; j < columns; j++) {
            if ((i + j) % 2 === 0) {
              result += '#';
            } else {
              result += '.';
            }
          }
          result += '\n';
        }
      
        return result;
      }
      
      const rows = 10;
      const columns = 11;
      const chessboard = generateChessboard(rows, columns);
      console.log(chessboard);
}
//************Методичка ии********************************
// Цей код використовує функцію generateChessboard, яка отримує два аргументи: rows (кількість рядків) і columns (кількість стовпців). Вкладені цикли for проходяться по кожній клітинці дошки. Умова (i + j) % 2 === 0 перевіряє, чи сума i і j є парною, і в залежності від цього встановлюється символ # або .. Рядок result поповнюється цими символами, а після кожного рядка додається символ нового рядка \n.
